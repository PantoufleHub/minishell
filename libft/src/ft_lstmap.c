#include "../libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*mapped;

	if (lst == NULL || f == NULL || del == NULL)
		return (NULL);
	mapped = NULL;
	while (lst)
	{
		ft_lstadd_back(&mapped, ft_lstnew(f(lst->content)));
		lst = lst->next;
	}
	return (mapped);
}
